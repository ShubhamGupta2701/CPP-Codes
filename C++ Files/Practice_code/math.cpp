#include<iostream>
#include<math.h>
using namespace std;
int main(){
    int a,b;
    cout<<"Enter 2 numbers: ";
    cin>>a>>b;
    cout<<"MAX of 2 numbers : "<<max(a,b)<<endl;
    cout<<"MIN of numbers : "<<min(a,b)<<endl;
    cout<<"power of function x^y : "<<pow(a,b)<<endl;
    cout<<"square root of number 1 : "<<sqrt(a)<<endl;
    cout<<"square root of number 2 : "<<sqrt(b)<<endl;
    return 0;
}